/**
 * @brief Sets the index parameter to the index corresponding to the provided
 * orientation.
 * @details If the orientation is not a valid direction, a value of -1 will be
 * returned by reference.
 * 
 * @param orientation The direction the object will be facing.
 * @param index The variable to store the index for which the orientation
 * corresponds to.
 */
#define getOrientIndex(orientation, index) \
	switch(orientation) \
	{ \
		case M_WEST: \
			index = 0;\
			break; \
		case M_EAST: \
			index = 1; \
			break; \
		case M_NORTH: \
			index = 2; \
			break; \
		case M_SOUTH: \
			index = 3; \
			break; \
		default: \
			index = -1; \
	}

