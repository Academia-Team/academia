const UINT16 *getFont16Digit(int digit, UINT16 *dest)
{
	const UINT16 *returnAddr = NULL;

	if (digit >=0 && digit < 10)
	{
		returnAddr = font16[digit + FONT16_NUM_IDX_START];

		if (dest != NULL)
		{
			memcpy(dest, returnAddr, FONT16_BYTE_SIZE);
		}
	}

	return returnAddr;
}

const UINT16 *getFont16Char(char character, UINT16 *dest)
{
	const UINT16 *returnAddr = NULL;

	if (character >= ' ' && character <= '~')
	{
		returnAddr = font16[character - ' '];

		if (dest != NULL)
		{
			memcpy(dest, returnAddr, FONT16_BYTE_SIZE);
		}
	}

	return returnAddr;
}